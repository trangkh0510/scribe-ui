"""
render.py — Đổ JSON từ analyzer skill vào template HTML thương hiệu ZaloPay.

Dùng:
    from render import render_report
    html = render_report(data)          # data = dict đã parse từ JSON của skill
    # hoặc CLI:  python render.py input.json > report.html

`data` phải có "mode" = "ux" | "meeting" | "cs". Xem JSON schema trong
skills-json-output.md để biết các field bắt buộc.

CSS lấy từ style.css cùng thư mục (đúng 2 màu brand: #0033C9 action, #00CF6A cue).
"""

import html as _html
import json
import os
import sys

_CSS_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), "style.css")


def esc(v) -> str:
    """Escape an toàn cho HTML. None -> ''."""
    return _html.escape(str(v if v is not None else ""))


# ---------- icon nhỏ (inline SVG) ----------
