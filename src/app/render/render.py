# nhãn brand + icon header cho mỗi mode
MODE_CFG = {
    "ux":      {"brand": "UX-Lens",       "eyebrow": "UX-Lens Report",      "foot": "Generated by UX-Lens \u00b7 Bermuda Team \u2014 ZaloPay"},
    "meeting": {"brand": "Meeting-Recap", "eyebrow": "Meeting Recap Report", "foot": "Generated by Meeting-Recap \u00b7 Bermuda Team"},
    "cs":      {"brand": "CS-Pulse",      "eyebrow": "CS-Pulse Report",      "foot": "Generated by CS-Pulse \u00b7 Bermuda Team"},
}

SEVERITY_LABEL = {
    "high": "HIGH", "medium": "MEDIUM", "low": "LOW",
    "crit": "CRITICAL", "major": "MAJOR", "minor": "MINOR",
}


# ---------- khối dùng chung ----------
def _action_bar(cfg) -> str:
    return f"""<div class="actionbar">
  <div class="brand"><span class="dot">{_icon(ICONS['search'])}</span>{esc(cfg['brand'])}</div>
  <div class="actions">
    <button class="btn" onclick="copyReport()">{_icon(ICONS['quote'])}<span id="copyLabel">Sao ch\u00e9p</span></button>
    <button class="btn primary" onclick="window.print()">{_icon(ICONS['doc'])}L\u01b0u PDF</button>
  </div>
</div>"""


def _header(cfg, meta) -> str:
    items = "".join(
        f'<div class="item"><div class="label">{esc(f["label"])}</div>'
        f'<div class="val">{esc(f["value"])}</div></div>'
        for f in meta.get("fields", [])
    )
    return f"""<div class="report-head">
  <span class="eyebrow">{esc(cfg['eyebrow'])}</span>
  <h1>{esc(meta.get('title'))}</h1>
  <p class="sub">{esc(meta.get('subtitle'))}</p>
  <div class="meta">{items}</div>
</div>"""


def _section_tab(num, title, tag) -> str:
    return (f'<div class="section-tab"><span class="num">{num}</span>'
            f'<h2>{esc(title)}</h2><span class="tag">{esc(tag)}</span></div>')


def _card(icon_key, title, inner) -> str:
    return (f'<div class="card"><div class="card-title">{_icon(ICONS[icon_key])}'
            f'{esc(title)}</div>{inner}</div>')


def _badge(severity) -> str:
    sev = (severity or "").lower()
    cls = {"high": "high", "medium": "medium", "low": "low",
           "crit": "crit", "major": "major", "minor": "minor"}.get(sev, "low")
    return f'<span class="badge {cls}"><span class="d"></span>{SEVERITY_LABEL.get(sev, esc(severity))}</span>'


def _verdict(v) -> str:
    lvl = (v.get("level") or "pass").lower()
    cls = {"pass": "pass", "warn": "warn", "fail": "fail"}.get(lvl, "pass")
    return (f'<div class="verdict"><span class="stamp {cls}">{esc(v.get("label"))}</span>'
            f'<span class="reason">{esc(v.get("reason"))}</span></div>')


def _scores(scores, total) -> str:
    rows = ""
    for s in scores:
        pts = int(s.get("score", 0))
        rows += (
            f'<div class="score-row"><div class="name">{esc(s.get("name"))}'
            f'<span>{esc(s.get("note"))}</span></div>'
            f'<div class="meter"><div class="bar"><i style="width:{pts*10}%"></i></div>'
            f'<span class="pts">{pts}/10</span></div></div>'
        )
    rows += (f'<div class="score-total"><span class="lbl">T\u1ed5ng \u0111i\u1ec3m</span>'
             f'<span class="big">{int(total)}<small>/40</small></span></div>')
    return _card("chart", "Ch\u1ea5m \u0111i\u1ec3m theo ti\u00eau ch\u00ed", rows)


def _suggestions(items, title="G\u1ee3i \u00fd c\u1ea3i thi\u1ec7n") -> str:
    lis = "".join(f"<li>{esc(x)}</li>" for x in items)
    return _card("bulb", title, f'<ul class="suggest">{lis}</ul>')


def _table(headers, rows_html) -> str:
    th = "".join(f"<th>{esc(h)}</th>" for h in headers)
    return f'<table><thead><tr>{th}</tr></thead><tbody>{rows_html}</tbody></table>'


# ---------- phần 1 theo từng mode ----------
def _part1_ux(d) -> str:
    out = []
    findings = ""
    for f in d.get("findings", []):
        tag = f'<span class="tagpill">{esc(f["tag"])}</span>' if f.get("tag") else ""
        ev = (f'<div class="ctx"><b>D\u1eabn ch\u1ee9ng:</b> \u201c{esc(f["evidence"])}\u201d</div>'
              if f.get("evidence") else "")
        findings += f'<li><span class="bullet"></span><div>{tag}{esc(f.get("text"))}{ev}</div></li>'
    out.append(_card("search", "Key findings", f'<ul class="findings">{findings}</ul>'))

    rows = ""
    for i, p in enumerate(d.get("painPoints", []), 1):
        ev = (f'<div class="ctx" style="margin-top:6px"><b>D\u1eabn ch\u1ee9ng:</b> \u201c{esc(p["evidence"])}\u201d</div>'
              if p.get("evidence") else "")
        rows += (f'<tr><td class="idx">{i}</td><td class="pp">{esc(p.get("pp"))}{ev}</td>'
                 f'<td>{_badge(p.get("severity"))}</td>'
                 f'<td class="flow">{esc(p.get("flow"))}</td></tr>')
    out.append(_card("warn", "Pain points",
                     _table(["#", "Pain point", "M\u1ee9c \u0111\u1ed9", "B\u01b0\u1edbc trong flow"], rows)))

    quotes = ""
    for q in d.get("quotes", []):
        quotes += (f'<div class="quote"><p>{esc(q.get("text"))}</p>'
                   f'<div class="ctx"><b>Ng\u1eef c\u1ea3nh:</b> {esc(q.get("ctx"))}</div></div>')
    out.append(_card("quote", "User quotes \u0111\u00e1ng ch\u00fa \u00fd", quotes))
    return "".join(out)


def _part1_meeting(d) -> str:
    out = []
    out.append(_card("doc", "T\u00f3m t\u1eaft nhanh",
                     f'<p style="font-size:14.5px;color:var(--ink-2);line-height:1.6">{esc(d.get("summary"))}</p>'))

    rows = ""
    for i, x in enumerate(d.get("decisions", []), 1):
        rows += (f'<tr><td class="idx">{i}</td><td class="pp">{esc(x.get("text"))}</td>'
                 f'<td class="status">{esc(x.get("who"))}</td></tr>')
    out.append(_card("check", "Decisions (\u0111\u00e3 ch\u1ed1t)",
                     _table(["#", "Quy\u1ebft \u0111\u1ecbnh", "Ai \u0111\u1ec1 xu\u1ea5t / ch\u1ed1t"], rows)))

    rows = ""
    for i, a in enumerate(d.get("actions", []), 1):
        rows += (f'<tr><td class="idx">{i}</td><td class="pp">{esc(a.get("task"))}</td>'
                 f'<td class="status">{esc(a.get("owner"))}</td>'
                 f'<td class="flow">{esc(a.get("deadline"))}</td></tr>')
    out.append(_card("task", "Action items",
                     _table(["#", "\u0110\u1ea7u vi\u1ec7c", "Owner", "Deadline"], rows)))

    if d.get("offtopic"):
        items = "".join(f'<li><span class="bullet"></span><div>{esc(x)}</div></li>'
                        for x in d["offtopic"])
        out.append(_card("quote", "Th\u1ea3o lu\u1eadn ngo\u00e0i l\u1ec1", f'<ul class="findings">{items}</ul>'))
    return "".join(out)


def _part1_cs(d) -> str:
    out = []
    out.append(_card("doc", "T\u00f3m t\u1eaft cu\u1ed9c g\u1ecdi",
                     f'<p style="font-size:14.5px;color:var(--ink-2);line-height:1.6">{esc(d.get("summary"))}</p>'))

    rows = ""
    senti_cls = {"neg": "neg", "neu": "neu", "pos": "pos"}
    for s in d.get("sentiment", []):
        cls = senti_cls.get((s.get("level") or "neu").lower(), "neu")
        rows += (f'<tr><td class="pp" style="white-space:nowrap">{esc(s.get("phase"))}</td>'
                 f'<td><span class="senti {cls}">{esc(s.get("label"))}</span></td>'
                 f'<td>{esc(s.get("signal"))}</td></tr>')
    senti_card = _table(["Giai \u0111o\u1ea1n", "Sentiment", "D\u1ea5u hi\u1ec7u trong transcript"], rows)
    if d.get("sentimentConclusion"):
        senti_card += (f'<p style="margin-top:14px;font-size:13.5px;color:var(--ink-2)">'
                       f'<b style="color:var(--zp-green-deep)">K\u1ebft lu\u1eadn:</b> {esc(d["sentimentConclusion"])}</p>')
    out.append(_card("smile", "Di\u1ec5n bi\u1ebfn sentiment kh\u00e1ch h\u00e0ng", senti_card))

    rows = ""
    for i, x in enumerate(d.get("issues", []), 1):
        rows += (f'<tr><td class="idx">{i}</td><td class="pp">{esc(x.get("issue"))}</td>'
                 f'<td>{_badge(x.get("severity"))}</td>'
                 f'<td class="status">{esc(x.get("status"))}</td></tr>')
    out.append(_card("warn", "V\u1ea5n \u0111\u1ec1 c\u1ee7a kh\u00e1ch h\u00e0ng",
                     _table(["#", "V\u1ea5n \u0111\u1ec1", "M\u1ee9c \u0111\u1ed9", "Tr\u1ea1ng th\u00e1i x\u1eed l\u00fd"], rows)))

    rows = ""
    for i, a in enumerate(d.get("actions", []), 1):
        rows += (f'<tr><td class="idx">{i}</td><td class="pp">{esc(a.get("task"))}</td>'
                 f'<td class="status">{esc(a.get("owner"))}</td>'
                 f'<td class="flow">{esc(a.get("deadline"))}</td></tr>')
    out.append(_card("task", "Action items",
                     _table(["#", "Vi\u1ec7c c\u1ea7n l\u00e0m", "Ai l\u00e0m", "Deadline"], rows)))

    if d.get("quotes"):
        quotes = ""
        for q in d["quotes"]:
            quotes += (f'<div class="quote"><p>{esc(q.get("text"))}</p>'
                       f'<div class="ctx"><b>Ng\u1eef c\u1ea3nh:</b> {esc(q.get("ctx"))}</div></div>')
        out.append(_card("quote", "Tr\u00edch \u0111o\u1ea1n \u0111\u00e1ng ch\u00fa \u00fd", quotes))
    return "".join(out)


