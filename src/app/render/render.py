def render_report(data: dict) -> str:
    """Nhận dict JSON từ analyzer skill -> trả chuỗi HTML hoàn chỉnh."""
    mode = (data.get("mode") or "").lower()
    if mode not in MODE_CFG:
        raise ValueError(f"mode kh\u00f4ng h\u1ee3p l\u1ec7: {mode!r} (ph\u1ea3i l\u00e0 ux|meeting|cs)")
    cfg = MODE_CFG[mode]

    with open(_CSS_PATH, encoding="utf-8") as fh:
        css = fh.read()

    p1_title, p1_tag = PART1_TAB[mode]
    p2_title, p2_tag = PART2_TAB[mode]

    body = (
        '<div class="wrap">'
        + _action_bar(cfg)
        + _header(cfg, data.get("meta", {}))
        + _section_tab(1, p1_title, p1_tag)
        + PART1[mode](data)
        + _section_tab(2, p2_title, p2_tag)
        + _verdict(data.get("verdict", {}))
        + _scores(data.get("scores", []), data.get("scoreTotal", 0))
        + _suggestions(data.get("suggestions", []), SUGGEST_TITLE[mode])
        + f'<p class="foot">{esc(cfg["foot"])}</p>'
        + "</div>"
    )

    return (
        '<!DOCTYPE html>\n<html lang="vi"><head>\n'
        '<meta charset="UTF-8"><meta name="viewport" content="width=device-width, initial-scale=1.0">\n'
        f'<title>{esc(cfg["brand"])} Report</title>\n'
        '<link rel="preconnect" href="https://fonts.googleapis.com">'
        '<link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>\n'
        '<link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&display=swap" rel="stylesheet">\n'
        f"<style>\n{css}\n</style></head><body>\n{body}\n{_SCRIPT}\n</body></html>\n"
    )


def main(argv):
    if len(argv) > 1:
        with open(argv[1], encoding="utf-8") as fh:
            data = json.load(fh)
    else:
        data = json.load(sys.stdin)
    sys.stdout.write(render_report(data))


if __name__ == "__main__":
    main(sys.argv)
